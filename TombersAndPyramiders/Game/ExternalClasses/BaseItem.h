/*========================================================================================
	Dependencies	
========================================================================================*/
class Inventory;

/*========================================================================================
	BaseItem	
========================================================================================*/
class BaseItem
{
    /*----------------------------------------------------------------------------------------
		Resource Management
    ----------------------------------------------------------------------------------------*/
    public:
        /** Default constructor. */
        explicit BaseItem() = default;

		virtual ~BaseItem() {};

    /*----------------------------------------------------------------------------------------
		Instance Methods
    ----------------------------------------------------------------------------------------*/
	public:
		virtual void addToInventory(Inventory* inventory) = 0;
};
