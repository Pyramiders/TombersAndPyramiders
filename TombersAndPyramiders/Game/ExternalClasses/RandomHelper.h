/*===================================================================================*//**
	RandomHelper
	
	Static class for generating random values.

    @author Erick Fernandez de Arteaga
	
*//*====================================================================================*/

#pragma once

/*========================================================================================
	Dependencies	
========================================================================================*/
#include <random>

/*========================================================================================
	RandomHelper	
========================================================================================*/
class RandomHelper
{
	/*----------------------------------------------------------------------------------------
		Class Fields
	----------------------------------------------------------------------------------------*/
	private:
		std::mt19937 generator = std::mt19937(std::random_device()());
		std::uniform_real_distribution<> dist0to1 = std::uniform_real_distribution<>(0, 1);

    /*----------------------------------------------------------------------------------------
		Resource Management
    ----------------------------------------------------------------------------------------*/
    public:
		/** Default constructor. */
		explicit RandomHelper() = default;
		~RandomHelper() {};

    /*----------------------------------------------------------------------------------------
		Instance Methods
    ----------------------------------------------------------------------------------------*/
	public:
		float random0to1()
		{
			return dist0to1(generator);
		}
};
